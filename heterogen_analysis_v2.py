"""# ── CELL 10: Analysis 4 — PCA"""

print("ANALYSIS 4: PCA on heterogeneity vectors")

scaler   = StandardScaler()
H_scaled = scaler.fit_transform(heterog)

n_components = min(10, H_scaled.shape[1])
pca    = PCA(n_components=n_components)
