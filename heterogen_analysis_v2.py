"""# ── CELL 6: Helper functions"""

def apply_bonferroni(p_values, n_tests=None):
    n = n_tests or len(p_values)
    return np.clip(np.array(p_values) * n, 0, 1)

def cohens_d_paired(a, b):
    diff = a - b
    return diff.mean() / (diff.std(ddof=1) + 1e-12)

def save_fig(fig, path):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    fig.savefig(path, dpi=150, bbox_inches="tight")
    print(f"  Saved: {path}")

"""# ── CELL 7: Analysis 1 — Paired t-tests per feature"""

print("ANALYSIS 1: Paired t-tests (central vs. peripheral)")

n_features = len(FEATURE_NAMES)
ttest_results = []

for i, fname in enumerate(FEATURE_NAMES):
    c = central[:, i]
    p = peripheral[:, i]

    # Drop NaN pairs (affect this feature only)
    valid = np.isfinite(c) & np.isfinite(p)
    c, p = c[valid], p[valid]

    t_stat, p_ttest = ttest_rel(c, p)
    try:
        w_stat, p_wilcox = wilcoxon(c - p)
    except ValueError:
        w_stat, p_wilcox = np.nan, np.nan

    ttest_results.append({
        "feature"         : fname,
        "n_valid"         : int(valid.sum()),
        "mean_central"    : c.mean(),
        "mean_peripheral" : p.mean(),
        "mean_diff"       : (c - p).mean(),
        "std_diff"        : (c - p).std(ddof=1),
        "cohens_d"        : cohens_d_paired(c, p),
        "t_statistic"     : t_stat,
        "p_ttest_raw"     : p_ttest,
        "w_statistic"     : w_stat,
        "p_wilcox_raw"    : p_wilcox,
    })

df_tests = pd.DataFrame(ttest_results)
df_tests["p_ttest_bonferroni"]  = apply_bonferroni(df_tests["p_ttest_raw"].values, n_features)
df_tests["p_wilcox_bonferroni"] = apply_bonferroni(df_tests["p_wilcox_raw"].fillna(1).values, n_features)
df_tests["significant_ttest"]   = df_tests["p_ttest_bonferroni"]  < 0.05
df_tests["significant_wilcox"]  = df_tests["p_wilcox_bonferroni"] < 0.05
df_tests = df_tests.sort_values("p_ttest_bonferroni")

n_sig = int(df_tests["significant_ttest"].sum())
print(f"Features significantly different (Bonferroni t-test p<0.05): {n_sig}/{n_features}")
print(f"\nTop 5 most significant features:")
print(df_tests[["feature", "mean_diff", "cohens_d", "p_ttest_bonferroni",
                 "significant_ttest"]].head(5).to_string(index=False))

out_csv = os.path.join(OUTPUT_DIR, "heterogeneity_paired_ttests.csv")
df_tests.to_csv(out_csv, index=False)
print(f"\nFull results saved to: {out_csv}")

# Plot
fig, ax = plt.subplots(figsize=(14, 8))
